int horse[4],mx,check[33],dice[10];
void go(int dice_n,int total)
{
    mx = max(total,mx);
    if(dice_n==10)return;
    for(int i=0;i<4;i++)
    {
        if(horse[i]==21) continue;
        int idx = horse[i],nxt_idx = mat[idx][dice[dice_n]], score=mat[nxt_idx][0];
        if(nxt_idx!=21 && check[nxt_idx]) continue;
        check[idx]=0; horse[i] = nxt_idx; check[nxt_idx]=1;
        go(dice_n+1,total+score);
        check[idx]=1; horse[i] = idx; check[nxt_idx]=0;
    }
}
